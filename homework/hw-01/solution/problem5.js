"use strict";

// Create a function that determines if a number is divisible by 10. It should return true or false

function divisible10(num) {
  return num % 10 === 0;
}

console.log(divisible10(10));
console.log(divisible10(20));
console.log(divisible10(25));
console.log(divisible10(15));
console.log(divisible10(42));
